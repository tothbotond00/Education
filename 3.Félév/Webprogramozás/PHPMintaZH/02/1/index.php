    function count_total($submissions, $type, $teacher_id) {
        return count(array_filter($submissions, function($e) use ($type, $teacher_id){
            return $e['type'] == $type && $e['teacher'] == $teacher_id;
        }));
    }

    function count_ungraded($submissions, $type, $teacher_id) {
        return count(array_filter($submissions, function($e) use ($type, $teacher_id){
            return $e['type'] == $type && $e['teacher'] == $teacher_id && $e['points'] === null;
        }));
    }

